Board = [
# 1   2   3   4   5   6   7   8
["-","-","-","-","-","-","-","-",],
["-","-","-","-","-","-","-","-",],
["-","-","-","-","-","-","-","-",],
["-","-","-","B","W","-","-","-",],
["-","-","-","W","B","-","-","-",],
["-","-","-","-","-","-","-","-",],
["-","-","-","-","-","-","-","-",],
["-","-","-","-","-","-","-","-",]
]

def display_board():
    # Print column labels
    print("         ", end="")
    for col in range(1,9):
        print(col, end="    ")
    print("\n     +-------------------------------------------+")

    # Print rows and cells
    for row in range(len(Board)):
        print(row+1, end="    |   ")
        for cell in Board[row]:
            print(cell, end="    ")
        print("|\n     |                                           |")
    print("     +-------------------------------------------+")


display_board()